    private void preencheTabelaPedido() {
        DefaultTableModel modelo = (DefaultTableModel) tLocalizaTudo.getModel();
        int i = modelo.getRowCount();
        while (i-- > 0) {
            modelo.removeRow(i);
        }
        List<Pedido> lista;
        lista = pd.pesquisa();
        for (Pedido p : lista) {
            modelo.addRow(new Object[]{p.getId(), p.getIdGarcom(), p.getIdProduto(), p.getIdMesa(), p.getQuantidade(), p.getEstado()});
        }
    }

